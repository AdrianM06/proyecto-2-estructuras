    @Override
    public void paint(Graphics g) {
        super.paint(g);
        if (jPanel5 != null && ctrl != null) {
            dibujarArbol(jPanel5.getGraphics());
        }
    }

    private void dibujarArbol(Graphics g) {
        if (g == null) return;
        List<Estructura.Monticulo.Nodo> nodos = ctrl.cola.getElementos();
        if (nodos.isEmpty()) return;
        dibujarNodo(g, 0, jPanel5.getWidth() / 2, 30, jPanel5.getWidth() / 4, nodos);
    }

    private void dibujarNodo(Graphics g, int i, int x, int y, int off, List<Estructura.Monticulo.Nodo> lista) {
        if (i < lista.size()) {
            g.setColor(Color.BLACK);
            if (2 * i + 1 < lista.size()) g.drawLine(x, y, x - off, y + 50);
            if (2 * i + 2 < lista.size()) g.drawLine(x, y, x + off, y + 50);
            g.setColor(new Color(173, 216, 230));
            g.fillOval(x - 15, y - 15, 30, 30);
            g.setColor(Color.BLACK);
            g.drawOval(x - 15, y - 15, 30, 30);
            g.drawString(String.valueOf(i), x - 5, y + 5);
            dibujarNodo(g, 2 * i + 1, x - off, y + 50, off / 2, lista);
            dibujarNodo(g, 2 * i + 2, x + off, y + 50, off / 2, lista);
        }
    }
