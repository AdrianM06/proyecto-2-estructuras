    private void BtnCrearDocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnCrearDocActionPerformed
    String userNom = jList1.getSelectedValue();
        if (userNom == null) return;
        String nombreDoc = JOptionPane.showInputDialog(this, "Nombre del documento:");
        String paginas = JOptionPane.showInputDialog(this, "Páginas:");
        if (nombreDoc != null && paginas != null) {
            try {
                for (Modelo.Usuario u : ctrl.usuarios) {
                    if (u.nombre.equals(userNom)) {
                        u.documentos.add(new Modelo.Documento(nombreDoc, Integer.parseInt(paginas), "PDF", u));
                        break;
                    }
                }
                actualizarListaDocs();
            } catch (Exception e) { }
        }    }//GEN-LAST:event_BtnCrearDocActionPerformed

    private void btnimprimirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnimprimirActionPerformed
    String docNom = jList2.getSelectedValue();
        String userNom = jList1.getSelectedValue();
        if (docNom != null && userNom != null) {
            String nombreLimpio = docNom.replace(" [EN COLA]", "");
            for (Modelo.Usuario u : ctrl.usuarios) {
                if (u.nombre.equals(userNom)) {
                    for (Modelo.Documento d : u.documentos) {
                        if (d.nombre.equals(nombreLimpio) && !d.enCola) {
                            ctrl.enviarAImprimir(d, btnprioritario.isSelected());
                            actualizarTodo();
                            return;
                        }
                    }
                }
            }
        }
    }//GEN-LAST:event_btnimprimirActionPerformed

    private void BtnUsuarioManualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnUsuarioManualActionPerformed
String nombre = jTextField1.getText().trim();
    Object seleccionado = jComboBox1.getSelectedItem(); 

    if (nombre.isEmpty() || seleccionado == null) {
        JOptionPane.showMessageDialog(this, "Por favor, ingrese nombre y seleccione prioridad.");
        return;
    }

    try {
        // Limpiamos el texto: quitamos espacios y pasamos a MAYÚSCULAS
        String prioridadTexto = seleccionado.toString().trim().toUpperCase();
        
        // Buscamos la constante en el Enum de Modelo
        Modelo.TipoPrioridad tp = Modelo.TipoPrioridad.valueOf(prioridadTexto);
        
        // Agregamos el usuario al controlador
        ctrl.usuarios.add(new Modelo.Usuario(nombre, tp));
        
        // Refrescamos la lista visual
        actualizarListaUsuarios();
        
        // Limpiamos el campo de texto para el próximo usuario
        jTextField1.setText("");
        
    } catch (IllegalArgumentException e) {
        JOptionPane.showMessageDialog(this, "Error: La opción '" + seleccionado + "' no es válida.\n"
                + "Asegúrese de que el ComboBox solo tenga: ALTA, MEDIA o BAJA.");
    }    }//GEN-LAST:event_BtnUsuarioManualActionPerformed

