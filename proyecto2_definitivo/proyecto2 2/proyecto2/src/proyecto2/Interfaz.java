    private void actualizarListaUsuarios() { // <--- AQUÍ YA NO DARÁ ERROR
        modeloListaUsuarios.clear();
        if (ctrl.usuarios != null) {
            for (Modelo.Usuario u : ctrl.usuarios) {
                modeloListaUsuarios.addElement(u.nombre);
            }
        }
    }

    private void actualizarListaDocs() {
        modeloListaDocs.clear();
        String seleccionado = jList1.getSelectedValue();
        if (seleccionado != null) {
            for (Modelo.Usuario u : ctrl.usuarios) {
                if (u.nombre.equals(seleccionado)) {
                    for (Modelo.Documento d : u.documentos) {
                        String estado = d.enCola ? " [EN COLA]" : "";
                        modeloListaDocs.addElement(d.nombre + estado);
                    }
                    break;
                }
            }
        }
    }

    private void actualizarTodo() {
        // Usamos jTable2 que es la que se ve en tu diseño
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();
        model.setRowCount(0); 

        if (ctrl.cola != null) {
            for (Estructura.Monticulo.Nodo n : ctrl.cola.getElementos()) {
                model.addRow(new Object[]{
                    n.doc.id, 
                    n.doc.nombre, 
                    String.format("%.2f", n.etiqueta), 
                    n.doc.dueño.nombre
                });
            }
        }
        
        jLabel4.setText("Reloj: " + String.format("%.1f", ctrl.reloj) + " Seg");
        jPanel5.repaint(); 
        actualizarListaDocs();
    }

